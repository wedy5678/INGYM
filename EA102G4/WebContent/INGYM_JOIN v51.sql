--會員行事曆--
CREATE OR REPLACE VIEW MEM_TIME
AS
SELECT I_ORDER_NO ,
I_CLASS_NO ,
MEM_ID ,
RDATE ,
HR ,
P_COIN ,
IO_STATUS  FROM I_CLASS_ORDER WHERE IO_STATUS <2
UNION 
SELECT GD.G_ORDER_NO ,
GD.G_CLASS_NO ,
GO.MEM_ID,
GD.RDATE ,
GD.HR ,
GD.P_COIN ,
GD.GO_STATUS FROM GO_DETAIL GD
JOIN GROUP_ORDER GO ON GD.G_ORDER_NO = GO.G_ORDER_NO WHERE GO_STATUS<2;

--教練已被預約的時間(不包含休息時間)--
CREATE OR REPLACE VIEW TRAINER_RESERVATION
AS
SELECT I_ORDER_NO ,
IO.I_CLASS_NO ,
PRO_ID ,
RDATE ,
HR FROM I_CLASS_ORDER  IO 
JOIN INDIVIDUAL_CLASS IC ON IO.I_CLASS_NO = IC.I_CLASS_NO 
UNION 
SELECT G_TIME_NO,
 GC.G_CLASS_NO,
PRO_ID,
C_DATE,
HR FROM GROUP_CLASS GC
JOIN GROUP_HOUR GH ON GC.G_CLASS_NO = GH.G_CLASS_NO;

