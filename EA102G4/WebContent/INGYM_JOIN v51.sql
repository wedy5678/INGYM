SELECT G_TIME_NO,
 GC.G_CLASS_NO,
PRO_ID,
C_DATE,
HR, 0  FROM GROUP_CLASS GC
JOIN GROUP_HOUR GH ON GC.G_CLASS_NO = GH.G_CLASS_NO
UNION
SELECT CTIME_NO,
'------------',
PRO_ID,
CLOSE_DATE,
HR, 0 FROM DAYOFF;



